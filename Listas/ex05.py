valores = [23, 45, 67, 89, 12, 56, 78, 90, 34, 56]

quantidade = len(valores)
print("A quantidade de valores na lista é {}".format(quantidade))

valor_maximo = max(valores)
print("O valor maximo da lista é {}".format(valor_maximo))

valor_minimo = min(valores)
print("O valor minimo da lista é {}".format(valor_minimo))

soma_valores = sum(valores)
print("A soma dos valores é {}".format(soma_valores))

lista_nova = [58.5, 63.2, 71.3, 69.4, 68.2]

media = sum(lista_nova) / len(lista_nova)
print("A media dessa lista é {}.".format(media))